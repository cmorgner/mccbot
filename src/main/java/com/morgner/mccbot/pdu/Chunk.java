package com.morgner.mccbot.pdu;

/**
 * Represents a chunk in the minecraft protocol.
 * This class has its own serializer implementation.
 *
 * @author Christian Morgner
 */
public class Chunk {

	private int x = 0;
	private int y = 0;
	private int z = 0;
	private int size = 0;
	private byte[] data = null;

	public int getSize() {
		return size;
	}

	public void setSize(int size) {
		this.size = size;
	}

	public byte[] getData() {
		return data;
	}

	public void setData(byte[] data) {
		this.data = data;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getZ() {
		return z;
	}

	public void setZ(int z) {
		this.z = z;
	}
}
